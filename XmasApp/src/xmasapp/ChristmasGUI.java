    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // Take input from GUI
 
        Christmas xmas = new Christmas();

        xmas.setName(nameTf.getText()); 
        xmas.setIdea(ideaTf.getText()); 
        xmas.setUrl(urlTf.getText());  

        giftList.add(xmas);

        JOptionPane.showMessageDialog(null,"It has been added!");
        
        // To clear Text field once button is pressed.
        nameTf.setText("");
        ideaTf.setText("");
        urlTf.setText("");

    }//GEN-LAST:event_addBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // Variables created to take input from user to delete itens in the list
        String receiver, gift, link;
        
        
        receiver = nameTf.getText();
        gift = ideaTf.getText();
        link = urlTf.getText();


        for (int i = 0;i < giftList.size();i++) {

            if (giftList.get(i).getName().equals(receiver) && giftList.get(i).getIdea().equals(gift) && giftList.get(i).getUrl().equals(link)) {
                giftList.remove(i);

                JOptionPane.showMessageDialog(null, "Iten deleted!");

                break;
            } else {
                 JOptionPane.showMessageDialog(null, "An error has ocurred make sure you have NAME, GIFT IDEA and URL correct before pressing delete.");
            }

        }
        
        // To clear Text field once button is pressed.
        nameTf.setText("");
        ideaTf.setText("");
        urlTf.setText("");
 
    }//GEN-LAST:event_deleteBtnActionPerformed

