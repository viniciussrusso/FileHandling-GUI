    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        String search = searchTf.getText();

        for (Christmas christmas: giftList) {
            if (search.equalsIgnoreCase(christmas.getName())) {
                JOptionPane.showMessageDialog(null, "Name: " + christmas.getName() + "\n Gift Idea: " + christmas.getIdea() + "\n Link URL: " + christmas.getUrl());
                break;
            }
            else {
                JOptionPane.showMessageDialog(null, "Cannot be found!");
            }
        }
        
        // To clear Text field once button is pressed.
        searchTf.setText("");
    }//GEN-LAST:event_searchBtnActionPerformed

    private void displayBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayBtnActionPerformed
        String list = "";

        if (giftList.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No itens have been added yet");
            return;
        }

        for (Christmas christmas : giftList) {
            list = list + christmas.getName() + " ";
            list = list + christmas.getIdea() + " ";
            list = list + christmas.getUrl() + "\n";
        }

        JOptionPane.showMessageDialog(null,list);
    }//GEN-LAST:event_displayBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        FileOutputStream fStream;
        ObjectOutputStream oStream;

        try {;
            fStream = new FileOutputStream(filePath);
            oStream = new ObjectOutputStream(fStream);
            oStream.writeObject(giftList);
            oStream.close();

        } catch (Exception except) {
            JOptionPane.showMessageDialog(this, "Error!");
        }

        JOptionPane.showMessageDialog(null, "Saved into file");
    }//GEN-LAST:event_saveBtnActionPerformed

    private void loadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadBtnActionPerformed
        FileInputStream fileStream;
        ObjectInputStream objStream;

        try {
            fileStream = new FileInputStream(filePath);
            objStream = new ObjectInputStream(fileStream);
            
            Christmas christmas = new Christmas();
            giftList = (ArrayList<Christmas>) objStream.readObject();
            
            JOptionPane.showMessageDialog(null, "File loaded!");

            objStream.close();

        } 
        catch(FileNotFoundException e){
            JOptionPane.showMessageDialog(this, "File not found!");
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error!");
        }
    }//GEN-LAST:event_loadBtnActionPerformed

